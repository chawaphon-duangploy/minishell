#include "libft.h"

int	ft_isalpha(int c)
{
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		return (1);
	else
		return (0);
}
//The ft_isalpha function determines if an integer c represents 
// an alphabetic character (either lowercase or uppercase) in the ASCII table.